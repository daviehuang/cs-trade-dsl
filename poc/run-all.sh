#!/usr/bin/env bash
# 一键跑通整个 PoC：JS 内核(UI/BFF) + Java 内核(中台) + 跨语言一致性裁决
set -e
HERE="$(cd "$(dirname "$0")" && pwd)"

echo "╔════════════════════════════════════════════════════════╗"
echo "║ 1) JS 内核执行 FX RuleSet（UI 与 BFF 共用同一产物）      ║"
echo "╚════════════════════════════════════════════════════════╝"
( cd "$HERE/js" && [ -d node_modules ] || npm install --silent; node run-fx.js )

echo ""
echo "╔════════════════════════════════════════════════════════╗"
echo "║ 2) JS 内核跑 golden 向量                                 ║"
echo "╚════════════════════════════════════════════════════════╝"
( cd "$HERE/js" && node run-golden.js )

echo ""
echo "╔════════════════════════════════════════════════════════╗"
echo "║ 3) Java 内核（中台）跑同一份 golden 向量                  ║"
echo "╚════════════════════════════════════════════════════════╝"
( cd "$HERE/java" && javac Dsl.java && java Dsl )

echo ""
echo "╔════════════════════════════════════════════════════════╗"
echo "║ 4) 跨语言一致性裁决：JS(UI/BFF) vs Java(中台)            ║"
echo "╚════════════════════════════════════════════════════════╝"
node "$HERE/compare.mjs"
